from fastapi import status

# 成功响应
HTTP_OK = status.HTTP_200_OK
HTTP_CREATED = status.HTTP_201_CREATED
HTTP_ACCEPTED = status.HTTP_202_ACCEPTED
HTTP_NO_CONTENT = status.HTTP_204_NO_CONTENT

